let Day=5;

switch (Day) {
    case 0:
        console.log('sunday')
        break;
    case 1:
        console.log('monday')
        break;
    case 2:
        console.log('twesday')
        break;
    case 3:
        console.log('wednesday')
        break;
    case 4:
        console.log('thrusday')
        break;
    case 5:
        console.log('friday')
        break;
    case 6:
        console.log('saturday')
        break;
    default:
        console.log('invalid day')
        break;
}